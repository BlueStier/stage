    </p>
    </div><br>
    <br>

      <h5>Le cadastre :</h5>
    <br>

      <p>Ouvert au public, il permet de renseigner les propriétaires de parcelles bâties ou non bâties sur les superficies et les références cadastrales de leur terrain.<br>
      Les extraits de matrice cadastrale peuvent vous être fournis au service urbanisme de la mairie et ce uniquement, à titre personnel.<br>
      Le plan cadastral est également disponible en ligne sur le site <a href="http://www.cadastre-gouv.fr">www.cadastre-gouv.fr</a><br>
    </p>
   <br>
    <br>

      <h5>La demande d’occupation de voirie : </h5>
    <br>

      <p>Toute occupation de voirie (comme la pose d’une benne ou d’un échafaudage, l’abaissement d’une bordure…) doit faire l’objet d’une autorisation préalable.<br>
      Un formulaire à compléter est disponible aux services techniques de la ville (rue des 80 fusillés) et doit être déposé au moins 15 jours avant la date prévue des travaux.<br>
    </p>
    <br>
    <br>

      <h5>Le permis de construire :</h5>
    <br>

      <p>Certaines modifications ou travaux nécessitent une autorisation spécifique (permis de construire, permis de démolir, déclaration préalable) notamment :<br>
    <ul>
        <li>Pour toute construction, agrandissement d’une maison, d’un local d’activité</li>
        <li>changement de destination des locaux</li>
        <li>construction de garage, abri de jardin, pergola, aménagement de combles…</li>
        <li>pose d’une clôture</li>
        <li>pose d’une enseigne</li>
        <li> modification de l’aspect extérieur d’une construction (ravalement de façade, remplacement de menuiseries…)</li>
</ul>
<i>Les imprimés sont disponibles en mairie au service urbanisme.</i>
    </p>
