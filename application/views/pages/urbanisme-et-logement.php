        <p>Les communes ont des responsabilités très étendues en matière d’urbanisme.<br>
        Il est désormais peu d’actes relatifs à l’occupation des propriétés privées qui ne transitent pas par la mairie, soit à titre de demandes de renseignements préalables à des ventes, soit pour l’exercice du contrôle réglementaire lorsqu’il s’agit de projet de construction ou de division de terrains.
        </p>
        <h5>Le Plan Local d’Urbanisme (P.L.U.)</h5>
      <p>Le P.L.U. fixe les règles générales et les servitudes d’utilisation des sols. Le droit à la construction est en effet strictement réglementé et diffère selon les zones dans lesquelles se trouvent les parcelles.<br>
      Le P.L.U. de Oignies peut être consulté au service urbanisme de la mairie.<br>
      <br>
      Consultez:<br>
